#!/usr/bin/env node
/**
 * Background Quality Check Script
 * Runs asynchronously after task completion
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// Configuration
const CONFIG = {
  logFile: path.join(__dirname, '../logs/quality-check.log'),
  reportDir: path.join(__dirname, '../reports'),
  projectRoot: path.join(__dirname, '../../muh5'),
};

// Ensure directories exist
if (!fs.existsSync(path.dirname(CONFIG.logFile))) {
  fs.mkdirSync(path.dirname(CONFIG.logFile), { recursive: true });
}

if (!fs.existsSync(CONFIG.reportDir)) {
  fs.mkdirSync(CONFIG.reportDir, { recursive: true });
}

/**
 * Log message to file
 */
function log(message) {
  const timestamp = new Date().toISOString();
  const logMessage = `[${timestamp}] ${message}\n`;
  fs.appendFileSync(CONFIG.logFile, logMessage);
  console.log(logMessage.trim());
}

/**
 * Run command and capture output
 */
function runCommand(command, cwd = CONFIG.projectRoot) {
  try {
    const output = execSync(command, { 
      cwd, 
      encoding: 'utf8',
      stdio: 'pipe'
    });
    return { success: true, output };
  } catch (error) {
    return { 
      success: false, 
      output: error.stdout || error.message 
    };
  }
}

/**
 * Get recently changed files
 */
function getRecentlyChangedFiles() {
  const result = runCommand('git diff --name-only HEAD~1 HEAD');
  if (result.success) {
    return result.output.split('\n').filter(f => f.trim() && f.endsWith('.ts'));
  }
  return [];
}

/**
 * Run linter on changed files
 */
function runLinter(files) {
  log('Running linter...');
  if (files.length === 0) {
    log('No files to lint');
    return { passed: true, issues: [] };
  }

  const result = runCommand(`npx eslint ${files.join(' ')} --format json`);
  
  if (result.success) {
    log('✅ Linting passed');
    return { passed: true, issues: [] };
  } else {
    try {
      const issues = JSON.parse(result.output);
      log(`⚠️  Linting found ${issues.length} issues`);
      return { passed: false, issues };
    } catch {
      log('⚠️  Linting failed');
      return { passed: false, issues: [] };
    }
  }
}

/**
 * Run tests
 */
function runTests() {
  log('Running tests...');
  const result = runCommand('npm test -- --passWithNoTests --silent');
  
  if (result.success) {
    log('✅ Tests passed');
    return { passed: true };
  } else {
    log('❌ Tests failed');
    return { passed: false, output: result.output };
  }
}

/**
 * Check test coverage
 */
function checkCoverage() {
  log('Checking test coverage...');
  const result = runCommand('npm run test:coverage -- --silent');
  
  if (result.success) {
    // Parse coverage from output
    const coverageMatch = result.output.match(/All files\s+\|\s+([\d.]+)/);
    const coverage = coverageMatch ? parseFloat(coverageMatch[1]) : 0;
    
    if (coverage >= 80) {
      log(`✅ Coverage: ${coverage}% (>= 80%)`);
      return { passed: true, coverage };
    } else {
      log(`⚠️  Coverage: ${coverage}% (< 80%)`);
      return { passed: false, coverage };
    }
  } else {
    log('⚠️  Coverage check failed');
    return { passed: false, coverage: 0 };
  }
}

/**
 * Generate quality report
 */
function generateReport(results) {
  const timestamp = new Date().toISOString();
  const reportPath = path.join(
    CONFIG.reportDir, 
    `quality-check-${timestamp.split('T')[0]}.md`
  );

  const report = `# Background Quality Check Report
**Date:** ${timestamp}
**Status:** ${results.allPassed ? '✅ PASSED' : '❌ FAILED'}

---

## Results

### Linting
- **Status:** ${results.linting.passed ? '✅ Passed' : '❌ Failed'}
- **Files Checked:** ${results.filesChecked}
${results.linting.issues.length > 0 ? `- **Issues Found:** ${results.linting.issues.length}` : ''}

### Tests
- **Status:** ${results.tests.passed ? '✅ Passed' : '❌ Failed'}

### Coverage
- **Status:** ${results.coverage.passed ? '✅ Passed' : '⚠️  Below Threshold'}
- **Coverage:** ${results.coverage.coverage}%
- **Threshold:** 80%

---

## Summary

${results.allPassed 
  ? '✅ All quality checks passed!' 
  : '⚠️  Some quality checks failed. Please review and fix issues.'}

---

**Generated by:** background-quality-check.js
`;

  fs.writeFileSync(reportPath, report);
  log(`📄 Report generated: ${reportPath}`);
  
  return reportPath;
}

/**
 * Main execution
 */
async function main() {
  log('🚀 Starting background quality check...');
  
  const startTime = Date.now();
  
  // Get changed files
  const changedFiles = getRecentlyChangedFiles();
  log(`Found ${changedFiles.length} changed files`);
  
  // Run checks
  const results = {
    filesChecked: changedFiles.length,
    linting: runLinter(changedFiles),
    tests: runTests(),
    coverage: checkCoverage(),
  };
  
  results.allPassed = 
    results.linting.passed && 
    results.tests.passed && 
    results.coverage.passed;
  
  // Generate report
  const reportPath = generateReport(results);
  
  const duration = ((Date.now() - startTime) / 1000).toFixed(2);
  log(`✅ Quality check completed in ${duration}s`);
  log(`📊 Report: ${reportPath}`);
  
  // Exit with appropriate code
  process.exit(results.allPassed ? 0 : 1);
}

// Run main function
main().catch(error => {
  log(`❌ Error: ${error.message}`);
  process.exit(1);
});
